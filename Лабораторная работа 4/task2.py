def get_procent_count_char(dict_):
    procent_dict = {} #создаю новый словарь, в который будет вестись запись
    for char, value in dict_.items(): #перебираю ключи и значения в парах ключ - значения из словаря dict_
        procent_dict.setdefault(char, round(value / sum(dict_.values()) * 100, 2)) #задаю соотвествующее ключу значение, выраженное в процентах, округленное до сотых
    return procent_dict #возвращаю созданный словарь

