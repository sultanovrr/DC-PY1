def get_count_char(str_):
    str_ = str_.lower() #привожу к нижнему регистру
    dict_ = {} #создаю словарь, в который будет вестись запись
    for char in str_: #перебираю символы в строке
        if char.isalpha(): #проверяю, что символ является буквой
            dict_.setdefault(char, str_.count(char)) # задаю значение, соответствующее ключу
    return dict_ #возвращаю созданный словарь


